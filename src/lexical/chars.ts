
/* -------------------------------------------- */
/* COMMON CHARACTERS                            */
/* -------------------------------------------- */

/**
 * `''` –  Empty String
 */
export const NIL = '';

/**
 * `"` –  Double quotation character
 */
export const DQO = '"';

/**
 * `,` –  Comma Character
 */
export const COM = ',';

/**
 * `'` –  Single quotation character
 */
export const SQO = "'";

/**
 * ` ` – Single whitespace character
 */
export const WSP = ' ';

/**
 * `\t` – Single tab character
 */
export const TAB = '\t';

/**
 * `\n` – Newline character
 */
export const NWL = '\n';

/**
 * `\r\n` - CRLF character sequence
 */
export const CNL = '\r\n';
